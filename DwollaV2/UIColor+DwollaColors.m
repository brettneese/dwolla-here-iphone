#import "UIColor+DwollaColors.h"

@implementation UIColor (DwollaColors)

+(UIColor*)DwollaOrange
{
    return [UIColor colorWithRed:250.0f/255.0f green:90.0f/255.0f blue:10.0f/255.0f alpha:1.0f];
}

+(UIColor*)DwollaGray
{
    return [UIColor colorWithRed:190.0f/255.0f green:195.0f/255.0f blue:200.0f/255.0f alpha:1.0f];
}

+(UIColor*)DwollaDarkGray
{
    return [UIColor colorWithRed:65.0f/255.0f green:75.0f/255.0f blue:75.0f/255.0f alpha:1.0f];
}

+(UIColor*)DwollaCharcoal
{
    return [UIColor colorWithRed:35.0f/255.0f green:35.0f/255.0f blue:35.0f/255.0f alpha:1.0f];
}

+(UIColor*)DwollaBlue
{
    return [UIColor colorWithRed:15.0f/255.0f green:130.0f/255.0f blue:185.0f/255.0f alpha:1.0f];
}

+(UIColor*)DwollaClearGray
{
    return [UIColor colorWithRed:65.0f/255.0f green:75.0f/255.0f blue:75.0f/255.0f alpha:.80f];
}
@end
